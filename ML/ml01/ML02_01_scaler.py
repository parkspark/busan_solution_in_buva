# 6. Ridge / Lasso
st.header("6. 규제 모델 (Ridge & Lasso)")
st.markdown("""
규제(Regularization)는 과도하게 복잡해진 모델이 훈련 데이터에 지나치게 밀착되어 과적합되는 현상을 억제하기 위해, 모델의 가중치($w$) 크기를 강제로 제한하는 패널티를 손실 함수에 추가하는 기법입니다.

#### 규제 전 StandardScaler 표준화 필수성
Ridge와 Lasso 모델은 회귀 계수(가중치)의 수치적 크기 자체를 직접 억제하는 방식으로 작동합니다.
만약 피처마다 스케일이 제각각 다르다면 계수의 크기도 고르지 않게 분포하게 되므로 특정 피처에 대해서만 불합리하게 강한 패널티가 부과될 수 있습니다.
따라서 규제 모델을 적용하기 전에는 **반드시 StandardScaler를 거쳐 모든 피처의 스케일을 균등하게 통일**시켜 주어야만 공정하고 효과적인 규제가 이루어집니다.

#### Ridge vs Lasso 상세 비교
| 구분 | Ridge 회귀 (L2 규제) | Lasso 회귀 (L1 규제) |
| :--- | :--- | :--- |
| **규제 방식** | 계수의 제곱 합을 손실 함수에 추가 | 계수의 절대값 합을 손실 함수에 추가 |
| **손실 함수** | $MSE + \\alpha \\sum w^2$ | $MSE + \\alpha \\sum |w|$ |
| **계수 동작** | 가중치의 크기를 전체적으로 작게 유지만 함 | 불필요하고 영향력이 적은 가중치를 정확히 0으로 만듦 |
| **특성 선택** | 없음 (모든 피처를 약하게라도 사용) | 자동 특성 선택 (중요치 않은 피처 자동 제거) |
| **선호 상황** | 수집된 모든 피처가 비교적 고르게 유효할 때 | 데이터에 불필요하거나 중복된 피처가 다수 존재할 때 |
| **농어 데이터 최적화** | $\\alpha = 0.1$ 일 때 테스트 $R^2$ 최고치 (0.9828) | $\\alpha = 10$ 일 때 최적이며, 55개 특성 중 40~47개를 0으로 압축 |

#### 규제 강도 하이퍼파라미터 $\\alpha$
- **$\\alpha$가 커질 경우**: 가중치를 강하게 억제하므로 모델이 단순해집니다. 지나치게 크면 최적의 관계마저 잃어버리는 과소적합 상태가 유도됩니다.
- **$\\alpha$가 매우 작을 경우**: 규제 패널티가 거의 작용하지 않으므로 기본 최소제곱 선형 회귀 모델과 유사해져 다시 과적합 문제가 대두될 위험이 있습니다.
""")

