# 1. StandardScaler
st.header("1. StandardScaler")
st.markdown("""
StandardScaler는 피처의 평균을 0, 표준편차를 1로 변환하여 데이터를 표준 정규분포 형태로 만드는 전처리 기법입니다.

$$z = \\frac{x - \\mu}{\\sigma}$$

여기서 $x$는 원래 값, $\\mu$는 평균, $\\sigma$는 표준편차입니다. 변환 후의 값은 보통 -3에서 +3 정도의 범위로 통일됩니다.

#### 정규화 vs 표준화 비교
| 구분 | StandardScaler (표준화) | MinMaxScaler (정규화) |
| :--- | :--- | :--- |
| **공식** | $(x - \\mu) / \\sigma$ | $(x - min) / (max - min)$ |
| **범위** | 제한 없음 (평균 0, 표준편차 1) | 0 ~ 1 고정 |
| **이상치** | 이상치에 덜 민감함 | 이상치에 매우 민감함 |
| **사용 시점** | k-NN, 선형 회귀, Ridge, Lasso 등 | 이미지 픽셀 처리, 데이터의 고정 범위가 필수적일 때 |

농어 데이터에는 이상치가 포함되어 있을 가능성이 존재하며, 이후 적용할 Ridge나 Lasso 모델은 거리 기반으로 작동하므로 표준화 전처리인 StandardScaler를 사용합니다.

#### 단위 스케일 불균형 관점에서의 필요성
머신러닝 모델(특히 k-NN, SVM, K-Means 등 거리 기반 알고리즘)은 피처들의 스케일이 다르면 분석 결과가 심각하게 왜곡됩니다.
생선 데이터셋의 스케일 차이를 예시로 살펴보겠습니다.
- 길이 범위: 8 ~ 44 cm (최대 차이 36)
- 무게 범위: 6 ~ 1100 g (최대 차이 1094)

이 데이터를 그대로 유클리드 거리 공식에 대입하면 다음과 같습니다.

$$d = \\sqrt{(길이_1 - 길이_2)^2 + (무게_1 - 무게_2)^2}$$

무게 수치의 제곱이 길이에 비해 압도적으로 크기 때문에, 실제 계산 시 길이는 사실상 무시되고 무게 차이만 거리 계산에 영향을 줍니다.
실제 예시 데이터를 통해 수치적으로 확인해 보겠습니다.
- 포인트 A: [20.0, 500.0] (길이 20cm, 무게 500g)
- 포인트 B: [44.0, 501.0] (길이 44cm, 무게 501g)
- 새 샘플: [22.0, 499.0]

새 샘플은 길이 관점에서 보면 A와 매우 가깝습니다(차이 2cm). 하지만 원본 스케일 그대로 거리를 계산하면 다음과 같습니다.
- 새 샘플과 A의 거리: $\\sqrt{(22-20)^2 + (499-500)^2} = \\sqrt{4 + 1} \\approx 2.24$
- 새 샘플과 B의 거리: $\\sqrt{(22-44)^2 + (499-501)^2} = \\sqrt{484 + 4} \\approx 22.09$

무게 값들이 거의 비슷하여 길이의 수치 차이가 거리에 큰 영향을 미친 것처럼 보이지만, 만약 무게의 스케일 차이가 크게 벌어지는 다른 포인트들이 섞이면 무게의 압도적인 편차가 길이 피처를 완전히 가려버리게 됩니다. 이를 고르게 조율하기 위해 StandardScaler를 적용하여 공정한 척도에서 거리를 측정해야 합니다.

#### Data Leakage (데이터 누수) 관점에서의 필요성
머신러닝 학습 및 검증 과정에서 가장 중요하게 준수해야 할 원칙 중 하나는 훈련 세트와 테스트 세트의 철저한 분리입니다.
스케일러를 피팅할 때 절대 테스트 세트의 통계 정보(평균, 표준편차)가 반영되어서는 안 됩니다.

- **잘못된 방법 (데이터 누수 발생)**
  ```python
  ss = StandardScaler()
  train_scaled = ss.fit_transform(train_input)
  test_scaled = ss.fit_transform(test_input) # 위험! 테스트 데이터의 평균/표준편차가 새로 반영됨
  ```
  테스트 데이터의 통계 정보가 스케일러에 반영되는 것은 **미래 데이터를 미리 훔쳐본 것**과 같으며, **시험지를 미리 보고 공부한 후 시험을 치는 것**과 다르지 않아 모델 평가 점수를 신뢰할 수 없게 만듭니다.

- **올바른 방법**
  ```python
  ss = StandardScaler()
  train_scaled = ss.fit_transform(train_input)
  test_scaled = ss.transform(test_input) # 훈련 데이터의 평균/표준편차 통계로 테스트 데이터 변환
  ```
  오직 훈련 세트의 평균과 표준편차를 기준으로만 테스트 세트를 변환해야 모델이 테스트 세트의 정보를 전혀 보지 못한 상태에서 객관적인 검증을 수행할 수 있습니다. 이를 위반하면 모델 평가 신뢰도가 붕괴되고 실전 배포 시 성능이 급격히 저하됩니다.
""")

